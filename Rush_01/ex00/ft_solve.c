#include <unistd.h>

void	ft_solve(char *argv[])
{
	if (argv[1][16] == '4' && argv[1][24] == '1')
	{
		write(1, "1 2 3 4\n", 8);
	}
	if (argv[1][18] == '3' && argv[1][26] == '2')
	{
		write(1, "2 3 4 1\n", 8);
	}
	if (argv[1][20] == '2' && argv[1][28] == '2')
	{
		write(1, "3 4 1 2\n", 8);
	}
	if (argv[1][22] == '1' && argv[1][30] == '2')
	{
		write(1, "4 1 2 3\n", 8);
	}
	else
	{
		write(2, "error", 5);
	}
}
